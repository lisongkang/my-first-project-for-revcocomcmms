package com.maywide.core.cons;

/**
 * 
 * 分隔符常量
 * 
 */
public interface Separator {
	
	/**
	 * EQUAL :  <code>"|###|"</code>
	 */
	String EQUAL = "|###|";

	/**
	 * COLUMN :  <code>"|$$$|"</code>
	 */
	String COLUMN = "|$$$|";

	/**
	 * ROW :  <code>"|@@@|"</code>
	 */
	String ROW = "|@@@|";

	/**
	 * WAVE :  <code>"~"</code>
	 */
	String WAVE = "~";
	
	/**
	 * TINE  :  <code>"^"</code>
	 */
	String TINE = "^";

	/**
	 * COMMA :  <code>","</code>
	 */
	String COMMA = ",";

	/**
	 * QUOT :  <code>"'"</code>
	 */
	String QUOT = "'";

	/**
	 * COLON :  <code>":"</code>
	 */
	String COLON = ":";

	/**
	 * STAR :  <code>"*"</code>
	 */
	String STAR = "*";

	/**
	 * UPRIGHT :  <code>"|"</code>
	 */
	String UPRIGHT = "|";

	/**
	 * CHGROW :  <code>"\r\n"</code>
	 */
	String CHGROW = "\r\n";

	/**
	 * BLANK :  <code>" "</code>
	 */
	String BLANK = " ";

	/**
	 * SEMICOLON :  <code>";" </code>
	 */
	String SEMICOLON = ";";

}
